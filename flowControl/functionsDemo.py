# In Python, function is a group of related statements that perform a specific task.

# Function Declaration
def greetme(name):
    print("Hello," + name, "Welcome To Python Learning")


def addInt(a, b):
    print(a+b)


def subInt(a, b):
    return a - b


# Function calling
greetme("Satya")

addInt(8, 3)

print(subInt(8, 3))
