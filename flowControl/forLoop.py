# For Loop -

obj = [0, 1, 2, 3, 4]
for i in obj:
    print(i)

# For Loop - Sum of Five natural number

Total = 0
for j in range(1, 6):
    Total = Total + j
print("Total of five natural number is:", Total)

# For Loop - Print alternate number

for k in range(1, 10, 2):
    print(k)
print("##############")

for m in range(0, 10, 3):
    print(m)
